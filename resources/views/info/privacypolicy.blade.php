<h1 class="text-center py-2">
    Políticas de Privacidad de AcademyHub
</h1>
<p class="px-5">
    <i>Fecha de entrada en vigor: 1 de Junio del año 2023</i>
    <br>
    En AcademyHub, nos tomamos la privacidad de nuestros usuarios muy en serio. Esta Política de Privacidad describe cómo recopilamos, utilizamos y protegemos la información personal que se obtiene a través de nuestro sitio web. Al utilizar AcademyHub, aceptas las prácticas descritas en esta política.
</p>

<h3 class="text-center py-2">1. Información que recopilamos:</h3>

<p class="text-justify px-5">
    1.1 Información de registro: Al registrarte en AcademyHub, recopilamos tu nombre, dirección de correo electrónico, contraseña y otra información relevante para la creación de tu cuenta.
    <br>
    1.2 Información del perfil: Puedes proporcionar información adicional en tu perfil, como tu foto, descripción personal y detalles de contacto opcionales.
    <br>
    1.3 Vacantes y postulaciones: Para facilitar el proceso de carga de vacantes universitarias y las postulaciones de profesores, recopilamos la información relacionada con las vacantes, los requisitos, las descripciones y los datos proporcionados en las postulaciones.
    <br>
    1.4 Información de uso: Recopilamos información sobre cómo interactúas con AcademyHub, como las páginas que visitas, las funciones que utilizas y las acciones que realizas en el sitio web.
    <br>
    1.5 Información de la base de datos: Al utilizar una base de datos MySQL, se recopila y almacena información relacionada con las vacantes, postulaciones y otros datos necesarios para el funcionamiento de AcademyHub.

</p>

<h3 class="text-center py-2">2. Uso de la información:</h3>
<p class="text-justify px-5">
    2.1 Mejora y personalización del servicio: Utilizamos la información recopilada para mejorar y personalizar tu experiencia en AcademyHub, brindarte contenido relevante y realizar mejoras en nuestros servicios.
    <br>
    2.2 Comunicación: Podemos utilizar tu dirección de correo electrónico para enviarte información relacionada con AcademyHub, como actualizaciones, notificaciones de postulaciones y vacantes relevantes.
    <br>
    2.3 Análisis y estadísticas: Podemos utilizar la información recopilada para realizar análisis y generar estadísticas agregadas y anónimas sobre el uso de AcademyHub, con el fin de comprender mejor las tendencias y mejorar nuestros servicios.
</p>

<h3 class="text-center py-2">3. Compartir información:</h3>

<p class="text-justify px-5">
    3.1 Proveedores de servicios: Podemos compartir información con terceros que nos brindan servicios de apoyo, como alojamiento, procesamiento de pagos y análisis de datos. Estos proveedores de servicios tienen acceso a la información personal necesaria para llevar a cabo sus funciones, pero no pueden utilizarla para otros fines.
    <br>
    3.2 Cumplimiento legal: Podemos divulgar información personal si creemos de buena fe que dicha divulgación es necesaria para cumplir con la ley, responder a solicitudes legales o proteger los derechos, la propiedad o la seguridad de AcademyHub, sus usuarios u otros.
</p>
<br>
<h3 class="text-center py-2">4. Seguridad de la información:</h3>

<p class="text-justify px-5">
    4.1 Medidas de seguridad: Implementamos medidas de seguridad razonables para proteger la información personal y la base de datos de AcademyHub contra pérdida, uso indebido y acceso no autorizado. Esto incluye la implementación de controles de acceso, cifrado y salvaguardias técnicas.
    <br>
    4.2 Base de datos MySQL: La base de datos MySQL utilizada en AcademyHub está protegida mediante la aplicación de las mejores prácticas de seguridad, incluyendo el uso de contraseñas seguras, restricciones de acceso y actualizaciones regulares del software para abordar posibles vulnerabilidades.
</p>

<h3 class="text-center py-2">5. Retención de datos:</h3>
<p class="text-justify px-5">
    5.1 Conservación de datos: Mantenemos la información personal recopilada durante el tiempo necesario para cumplir con los fines descritos en esta Política de Privacidad, a menos que se requiera un período de retención más largo según lo establecido por la ley.
</p>
<h3 class="text-center py-2">6. Cambios en esta Política de Privacidad:</h3>
Podemos actualizar esta Política de Privacidad de vez en cuando. Te notificaremos sobre cambios significativos publicando la versión actualizada en AcademyHub o mediante otros medios de comunicación. Te recomendamos que revises esta política periódicamente.
<br>
<h3 class="text-center py-2">7. Contacto:</h3>
Si tienes alguna pregunta, inquietud o solicitud relacionada con esta Política de Privacidad, puedes comunicarte con nosotros a través de los canales de contacto proporcionados en AcademyHub.
